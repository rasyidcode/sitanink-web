<script>
    $(function() {
        $('#data-kartu').DataTable({
            dom: 'lrtip',
            searching: true,
            responsive: true,
            pageLength: 10,
            processing: true,
            serverSide: true,
            order: [],
            ajax: function(data, callback, settings) {
                var data = {
                    ...data,
                    ['<?= csrf_token() ?>']: '<?= csrf_hash() ?>'
                }
                $.ajax({
                    type: 'post',
                    url: '<?= route_to('api.card.get-data') ?>',
                    data: data,
                    beforeSend: function(xhr) {
                        xhr.setRequestHeader('Authorization', 'Basic ' + btoa('sitaninkadmin:admin123'));
                    },
                    success: function(res) {
                        console.log(res);
                        callback(res);
                    },
                    error: function(err) {
                        console.log(err);
                        callback([]);
                    }
                });
            },
            columns: [{
                    targets: 0,
                    orderable: false,
                    searchable: false
                },
                {
                    targets: 1,
                    orderable: true,
                    searchable: true
                },
                {
                    targets: 2,
                    orderable: true,
                    searchable: true
                },
                {
                    targets: 3,
                    orderable: true,
                    searchable: true
                },
                {
                    targets: 4,
                    orderable: false,
                    searchable: false
                },
                {
                    targets: 5,
                    orderable: false,
                    searchable: false
                },
                {
                    targets: 6,
                    orderable: false,
                    searchable: false
                },
                {
                    targets: 7,
                    orderable: false,
                    searchable: false
                },
            ],
            drawCallback: function(settings) {}
        });

        // $('#data-kartu tbody').on('click', 'button.btn.btn-success', function(e) {
        //     console.log('clicked');
        // });

        $('#modal-show-image').on('show.bs.modal', function(e) {
            var relatedTarget = $(e.relatedTarget).data();
            var idBerkas = relatedTarget.idBerkas;

            console.log(idBerkas);
            var $thisElement = $(this);

            $.ajax({
                type: 'get',
                url: `<?= site_url('api/v1/card/get-berkas') ?>/${idBerkas}`,
                beforeSend: function(xhr) {
                    xhr.setRequestHeader('Authorization', 'Basic ' + btoa('sitaninkadmin:admin123'));
                },
                success: function(res) {
                    console.log(res);

                    if (res.data) {
                        $thisElement.find('img.front')
                               .attr('src', `<?= site_url('kartu') ?>/${res.data.filename}`)
                               .attr('alt', 'Front Card');
                        $thisElement.find('img.back')
                               .attr('src', `<?= site_url('kartu') ?>/back-card.png`)
                               .attr('alt', 'Back Card');
                    } else {
                        alert('Failed to show image!');
                    }
                },
                error: function(err) {
                    console.log(err);

                    alert('Failed to show image!');
                }
            })
        });
    });
</script>
